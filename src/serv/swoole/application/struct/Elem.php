namespace app\struct;

/**
 * Class Elem
 * @package app\struct
 */
class Elem extends Struct
{
    // 消息文本
    private string $text;

    /**
     * @return string
     */
    public function getText(): string
    {
        return $this->text;
    }

    /**
     * @param string $text
     */
    public function setText(string $text): void
    {
        $this->text = $text;
    }
}
